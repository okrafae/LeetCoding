class Solution{
  public:
  long long int wineSelling(vector<int>& Arr, int N){
      //Code here.
      
      long long int ans = 0, currSum = 0;
      for( int i = 0; i < N; i++)
      {
          currSum += Arr[i];
          ans += abs(currSum);
      }
      return ans;
  }
};

